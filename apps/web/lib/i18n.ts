export function getMessage(lang: AppLanguage, key: string): string {
  return MESSAGES[lang][key] ?? MESSAGES.en[key] ?? key;
}

export function getStatusLabel(lang: AppLanguage, status: string | null | undefined): string {
  const normalized = (status || "unknown").toLowerCase();
  return (
    STATUS_LABELS[lang][normalized] ??
    STATUS_LABELS.en[normalized] ??
    normalized.replaceAll("_", " ")
  );
}
